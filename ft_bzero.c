#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptr;
	size_t	i;

	if (!s)
		return ;
	i = 0;
	ptr = (char *)s;
	while (i < n)
		ptr[i++] = 0;
}
